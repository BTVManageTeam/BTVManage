/**菜单管理js*/

/**
 * 配置菜单
 */
function toConfigMenu(id){
	$.Mark.show();
	$.ajax({
		type : 'POST',
		url : ctx + "/menu/toMenu/?systemId=" + id,
		cache : false,
		success : function(response) {
			$.Mark.hide();
			$("#content").css("display", "block");
			$("#contenttwo").remove();
			$("#contentthree").remove();
			$("#content").html(response);
			$.Mark.hide();
		},
		error : function(a, b, c) {
			$.YSAjax.error(a, b, c);
		}
	});
}
